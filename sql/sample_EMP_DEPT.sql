SELECT EMP.JOB,DEPT.DNAME,COUNT(*) AS empCount
FROM EMP INNER JOIN DEPT
ON EMP.DEPTNO=DEPT.DEPTNO
WHERE DEPT.DEPTNO=10 OR DEPT.DEPTNO=20
GROUP BY EMP.JOB,DEPT.DNAME
HAVING COUNT(*) > 1
ORDER BY 3 DESC;

SELECT a.ENAME AS 'NAME',b.ENAME AS 'BOSS'
FROM EMP AS a LEFT JOIN EMP AS b
ON a.MGR = b.EMPNO;

SELECT EMP1.ENAME AS 'EMPLOYEE_NAME',EMP2.ENAME AS 'MANAGER_NAME',DEPT.DNAME AS 'DEPARTMENT_NAME'
FROM (EMP AS EMP1 LEFT JOIN EMP AS EMP2
ON EMP1.MGR = EMP2.EMPNO)INNER JOIN DEPT
ON EMP1.DEPTNO=DEPT.DEPTNO;

